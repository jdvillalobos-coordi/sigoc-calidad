export function getPersona(id: string): Persona | undefined {
  return personas.find((p) => p.id === id);
}

export function getPersonaPorCedula(cedula: string): Persona | undefined {
  return personas.find((p) => p.cedula === cedula);
}

export function getVehiculo(id: string): Vehiculo | undefined {
  return vehiculos.find((v) => v.id === id);
}

export function getVehiculoPorPlaca(placa: string): Vehiculo | undefined {
  return vehiculos.find((v) => v.placa === placa);
}

export function getGuia(numero: string): Guia | undefined {
  return guias.find((g) => g.numero === numero);
}

export function getRegistrosPorPersona(personaId: string): Registro[] {
  return registros.filter(
    (r) =>
      r.personasVinculadas?.some((pv) => pv.personaId === personaId) ||
      ("cedula" in r && (r as RegistroContacto).cedula === getPersona(personaId)?.cedula)
  );
}

export function getRegistrosPorGuia(guia: string): Registro[] {
  return registros.filter((r) => "guia" in r && (r as any).guia === guia);
}

export function getRegistrosPorVehiculo(vehiculoId: string): Registro[] {
  return registros.filter((r) =>
    r.vehiculosVinculados?.some((vv) => vv.vehiculoId === vehiculoId)
  );
}

export function getRegistrosRelacionados(registroId: string): Registro[] {
  const reg = registros.find((r) => r.id === registroId);
  if (!reg) return [];
  const resultado: Registro[] = [];
  const guia = "guia" in reg ? (reg as any).guia : null;
  if (guia) {
    registros.forEach((r) => {
      if (r.id !== registroId && "guia" in r && (r as any).guia === guia) {
        resultado.push(r);
      }
    });
  }
  if (reg.personasVinculadas) {
    reg.personasVinculadas.forEach((pv) => {
      registros.forEach((r) => {
        if (
          r.id !== registroId &&
          !resultado.find((x) => x.id === r.id) &&
          r.personasVinculadas?.some((pv2) => pv2.personaId === pv.personaId)
        ) {
          resultado.push(r);
        }
      });
    });
  }
  return resultado.slice(0, 5);
}

