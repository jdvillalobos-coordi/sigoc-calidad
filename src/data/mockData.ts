export function getPersona(id: string): Persona | undefined {
  return personas.find((p) => p.id === id);
}

export function getPersonaPorCedula(cedula: string): Persona | undefined {
  return personas.find((p) => p.cedula === cedula);
}

export function getVehiculo(id: string): Vehiculo | undefined {
  return vehiculos.find((v) => v.id === id);
}

export function getVehiculoPorPlaca(placa: string): Vehiculo | undefined {
  return vehiculos.find((v) => v.placa === placa);
}

export function getGuia(numero: string): Guia | undefined {
  return guias.find((g) => g.numero === numero);
}

export function getEventosPorPersona(personaId: string): Evento[] {
  return eventos.filter(
    (e) =>
      e.personasResponsables.some((p) => p.personaId === personaId) ||
      e.personasParticipantes.some((p) => p.personaId === personaId)
  );
}

export function getEventosPorGuia(guia: string): Evento[] {
  return eventos.filter((e) => e.guias?.includes(guia));
}

export function getEventosPorVehiculo(vehiculoId: string): Evento[] {
  return eventos.filter((e) =>
    e.vehiculosVinculados?.some((vv) => vv.vehiculoId === vehiculoId)
  );
}

export function getEventosRelacionados(eventoId: string): Evento[] {
  const ev = eventos.find((e) => e.id === eventoId);
  if (!ev) return [];
  const resultado: Evento[] = [];
  // Por eventos asociados explícitos
  if (ev.eventosAsociados) {
    ev.eventosAsociados.forEach((id) => {
      const related = eventos.find((e) => e.id === id);
      if (related) resultado.push(related);
    });
  }
  // Por guías compartidas
  if (ev.guias && ev.guias.length > 0) {
    eventos.forEach((e) => {
      if (e.id !== eventoId && !resultado.find((x) => x.id === e.id)) {
        if (e.guias?.some((g) => ev.guias!.includes(g))) {
          resultado.push(e);
        }
      }
    });
  }
  // Por personas responsables compartidas
  ev.personasResponsables.forEach((pr) => {
    eventos.forEach((e) => {
      if (e.id !== eventoId && !resultado.find((x) => x.id === e.id)) {
        if (e.personasResponsables.some((p) => p.personaId === pr.personaId)) {
          resultado.push(e);
        }
      }
    });
  });
  return resultado.slice(0, 5);
}

